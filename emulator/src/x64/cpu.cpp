    void Cpu::execRet(const X64Instruction&) {
        regs_.rip() = pop64();
        vm_->notifyRet();
    }

    void Cpu::execRetImm(const X64Instruction& ins) {
        const auto& src = ins.op0<Imm>();
        regs_.rip() = pop64();
        regs_.rsp() += get<u64>(src);
        vm_->notifyRet();
    }

    void Cpu::execLeave(const X64Instruction&) {
        regs_.rsp() = regs_.rbp();
        regs_.rbp() = pop64();
    }

